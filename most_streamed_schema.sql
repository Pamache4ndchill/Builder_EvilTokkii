-- Script SQL para Supabase Dashboard
-- 1. Crear la tabla
CREATE TABLE IF NOT EXISTS public.most_streamed (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    image_url TEXT NOT NULL,
    order_index INT DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Habilitar RLS
ALTER TABLE public.most_streamed ENABLE ROW LEVEL SECURITY;

-- 3. Política de lectura pública
CREATE POLICY "Public Read Access" 
ON public.most_streamed 
FOR SELECT 
USING (true);

-- 4. Política de edición para usuarios autenticados
CREATE POLICY "Enable all access for authenticated users" ON public.most_streamed
FOR ALL USING (true) WITH CHECK (true);

